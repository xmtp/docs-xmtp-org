export default function PageActions() {
  const { content } = usePageData();
  const location = useLocation();
  const [copied, setCopied] = useState(false);
  const [origin, setOrigin] = useState('');
  const isHomepage = location.pathname === '/';

  useEffect(() => {
    setOrigin(window.location.origin);
  }, []);

  useEffect(() => {
    if (!copied) return;
    const timeout = setTimeout(() => setCopied(false), 1500);
    return () => clearTimeout(timeout);
  }, [copied]);

  const copy = useCallback(() => {
    navigator.clipboard.writeText(content ?? '');
    setCopied(true);
  }, [content]);

  const pageURL = origin + location.pathname;

  if (isHomepage) return null;

  const prompt = `Please research and analyze this page: ${pageURL} so I can ask you questions about it.`;
  const encodedPrompt = encodeURIComponent(prompt);

  const chatGPTLink = `https://chatgpt.com?hints=search&q=${encodedPrompt}`;
  const claudeLink = `https://claude.ai/new?q=${encodedPrompt}`;

  return (
    <div style={{ display: 'flex', alignItems: 'center', flexWrap: 'wrap', gap: '8px', marginTop: '4px', marginBottom: '16px' }}>
      <button
        type="button"
        onClick={copy}
        className={buttonClass}
        style={buttonStyle}
      >
        <div style={iconSize}>
          {copied ? <CheckCircleIcon /> : <CopyIcon />}
        </div>
        {copied ? 'Copied!' : 'Copy page'}
      </button>
      <a
        href={chatGPTLink}
        target="_blank"
        rel="noopener noreferrer"
        className={buttonClass}
        style={buttonStyle}
      >
        <div style={iconSize}>
          <OpenAiIcon />
        </div>
        Ask in ChatGPT
      </a>
      <a
        href={claudeLink}
        target="_blank"
        rel="noopener noreferrer"
        className={buttonClass}
        style={buttonStyle}
      >
        <div style={iconSize}>
          <ClaudeIcon />
        </div>
        Ask in Claude
      </a>
    </div>
  );
}
